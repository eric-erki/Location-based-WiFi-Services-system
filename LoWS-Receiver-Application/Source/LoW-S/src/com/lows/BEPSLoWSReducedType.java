	@Override
	public String decodeData(String dataInHex) {
		
		String returnText;
		StringBuilder bepsID = new StringBuilder();
		for(int p = 0; p < dataInHex.length(); p+=2)
		{
			String str = dataInHex.substring(p, p+2);
			bepsID.append((char)Integer.parseInt(str, 16));
		}
		if(bepsID.charAt(0)=='R')
		{
			returnText = "CODE RED: Fire Emergency!";
		}
		else if(bepsID.charAt(0)=='Z')
		{
			returnText = "CODE ZERO: No Emergency";
		}
		else if(bepsID.charAt(0)=='b')
		{
			returnText = "CODE BROWN: Severe Weather Emergency!";
		}
		else if(bepsID.charAt(0)=='G')
		{
			returnText = "CODE GREEN: Internal Disaster!";
		}
		else if(bepsID.charAt(0)=='B')
		{
			returnText =  "CODE BLACK: Bomb Threat!";
		}
		else if(bepsID.charAt(0)=='W')
		{
			returnText = "CODE WHITE: Evacuation!";
		}
		else if(bepsID.charAt(0)=='P')
		{
			returnText = "CODE PINK: Child Abduction!";
		}
		else if(bepsID.charAt(0)=='O')
		{
			returnText =  "CODE ORANGE: Aggressive Situation !";
		}
		else if(bepsID.charAt(0)=='Y')
		{
			returnText = "CODE YELLOW: Medical Crisis !";
		}
		else
		{
			returnText = "Not supported BEPS ID";
		}
		return returnText;
	}

